export interface ThemePreset {
  id: string;
  name: string;
  light: Record<string, string>;
  dark: Record<string, string>;
}

export interface SavedTheme extends ThemePreset {
  savedAt: number;
  isCustom: true;
}

const SAVED_THEMES_KEY = 'orbrh-saved-themes';

export function getSavedThemes(): SavedTheme[] {
  try {
    const raw = localStorage.getItem(SAVED_THEMES_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

export function saveTheme(theme: SavedTheme): void {
  const themes = getSavedThemes();
  const existingIndex = themes.findIndex(t => t.id === theme.id);
  if (existingIndex >= 0) {
    themes[existingIndex] = theme;
  } else {
    themes.unshift(theme);
  }
  localStorage.setItem(SAVED_THEMES_KEY, JSON.stringify(themes));
}

export function deleteSavedTheme(id: string): void {
  const themes = getSavedThemes().filter(t => t.id !== id);
  localStorage.setItem(SAVED_THEMES_KEY, JSON.stringify(themes));
}

